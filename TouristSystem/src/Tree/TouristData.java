package Tree;

public class TouristData { //游客信息
	public String num; //游客号
	public String name; //游客姓名
	public String sex; //游客性别
	public String age; //游客年龄
	public String origin; //游客来源地

	public TouristData(String num, String name, String sex, String age, String origin) {
		this.num = num;
		this.name = name;
		this.sex = sex;
		this.age = age;
		this.origin = origin;
	}
}